# 함수정의
def add(x,y):
    result = x + y
    print(result)

def sub(x,y):
    result = x - y
    print(result)

def mul(x,y):
    result = x * y
    print(result)

def div(x,y):
    result = x // y
    print(result)

def hello():
    print('Hello')

def hello2():
    msg = 'hello, hello'
    return msg
hello()
print(hello2())
add(1000,10)
